    @Override
    @Transactional
    public String register(RegistrationRequest registrationRequest) throws UsernameAlreadyExistsException, EmailNotValidException, MailNotSentException {

        ApplicationUser applicationUser = new ApplicationUser(
                                                registrationRequest.getUsername(),
                                                registrationRequest.getPassword(),
                                                registrationRequest.getEmail(),
                                                registrationRequest.getFirstName(),
                                                registrationRequest.getLastName(),
                                //                        registrationRequest.getBirthDate(),
                                                50000.0,
                                                Role.USER);
        String token = applicationUserService.signUpUser(applicationUser);


        String link = "http://localhost:3000/account/verify-email?token=" + token;

        emailSender.send(
                registrationRequest.getEmail(),
                buildRegisterEmail(registrationRequest.getFirstName(), link), "Confirmation Link for New User");

        LocalDate localDate = LocalDate.now();
        LocalTime localTime = LocalTime.now();

        emailSender.send(
                "alphatrader@mail.com",
                buildRegisterEmailToAdmin(applicationUser, localDate, localTime), "New User Creation");
        return token;
    }

    @Override
    @Transactional
    public boolean unregister(Long applicationUserId, String email) throws MailNotSentException {

        ApplicationUser applicationUser = applicationUserService.getOneUser(applicationUserId);

        LocalDate localDate = LocalDate.now();
        LocalTime localTime = LocalTime.now();

        emailSender.send(
                "alphatrader@mail.com",
                buildUnregisterEmailToAdmin(applicationUser, localDate, localTime, email), "Unregister User Request");
        return false;
    }

